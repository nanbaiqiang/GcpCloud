package com.lxg.spring.vo;

public class AreaVO {

	String year;
	String pay;
	String sumIns;
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getPay() {
		return pay;
	}
	public void setPay(String pay) {
		this.pay = pay;
	}
	public String getSumIns() {
		return sumIns;
	}
	public void setSumIns(String sumIns) {
		this.sumIns = sumIns;
	}
	
}
